import Foundation
import CoreData
 

extension Tag {

    @nonobjc public class func fetchRequest() -> NSFetchRequest<Tag> {
        return NSFetchRequest<Tag>(entityName: "Tag");
    }

    @NSManaged public var tagName: String?
    @NSManaged public var book: Book?

}

